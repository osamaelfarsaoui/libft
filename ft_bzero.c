#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*tmp;

	tmp = (unsigned char *)s;
	i = 0;
	while (n > 0)
	{
		tmp[i] = 0;
		i++;
		n--;
	}
}
